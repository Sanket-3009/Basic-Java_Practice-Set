public class SkipTheEvenNum {
    //Skip the Evens Write a program that uses a for loop to iterate from 1 to 20. Print the number, but skip all the even numbers.
    //
    public static void main(String[] args) {
        for (int i=1;i<=20;i++){

            if (i%2!=0){
                System.out.println(i);

            }
        }
    }

}
